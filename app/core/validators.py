import re

# ==============================================================================
# 手机区号 (Phone Code)
# ==============================================================================

# 格式：以 + 开头，后接 1-4 位数字
PHONE_CODE_PATTERN = re.compile(r"^\+\d{1,4}$")
PHONE_CODE_ERROR_MESSAGE = "手机区号格式错误，应以 + 开头，后跟 1-4 位数字"

# ==============================================================================
# 手机号 (Mobile)
# ==============================================================================

# 格式：纯数字，5 到 15 位（兼容国际号码长度差异）
MOBILE_PATTERN = re.compile(r"^\d{5,15}$")
MOBILE_ERROR_MESSAGE = "手机号格式错误，应为 5-15 位纯数字"
